package org.example.day02.ch04;

public class PrefixOp {
    public static void main(String[] args) {
        int num = 7;
        System.out.println("num: " + num);

        ++num;
        ++num;
        System.out.println("num: " + num);

        float num2 = 7;
        ++num2;
        System.out.println("num2: " + num2);

        int num3 = 10;
        --num3;
        System.out.println("num3: " + num3);
    }
}
